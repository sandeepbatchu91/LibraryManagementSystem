  public static int createborrower(Connection connection,PreparedStatement preparedstatement,int cardno,String fn,String ln,String addr,String phn)
   throws SQLException
   {
		    preparedstatement=connection.prepareStatement("insert into borrower(card_no,fname,lname,address,phone) values(?,?,?,?,?)");
		
                    preparedstatement.setInt(1,cardno);
                    preparedstatement.setString(2,fn);
                    preparedstatement.setString(3,ln);
                    preparedstatement.setString(4,addr);
                    preparedstatement.setString(5,phn);
                    
		    int k=preparedstatement.executeUpdate();
                    return k;
   }  
    
    
