        // TODO add your handling code here:
        String fname="",lname="",address="",phn="";
        if(FnTF.getText().isEmpty()||LnTF.getText().isEmpty()||ATF.getText().isEmpty())
        {
                jLabel8.setForeground(Color.red);
                jLabel8.setText("First Name,Lname and Address fields are required");
        }
        else 
        {
            if(PTF.getText().isEmpty())
            {
                phn=null;
            }
            else
            {
                if(PTF.getText().length()!= 10)
                {
                    jLabel7.setForeground(Color.red);
                    jLabel7.setText("It is not a valid phone number");
                }
                else
                {
                    jLabel7.setForeground(Color.blue);
                    jLabel7.setText("valid phone number");
                    String area_code=PTF.getText().substring(0,3);
                    String b=PTF.getText().substring(3,6);
                    String c=PTF.getText().substring(6);
                    phn= "("+area_code+") "+b+"-"+c;
                    System.out.println(phn);
                 }
            }
            fname=FnTF.getText();
            lname=LnTF.getText();
            address=ATF.getText();
            try
            {
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/", "root", "2934991");
            Statement stmt=conn.createStatement();
            PreparedStatement preparedstatement = null;
            stmt.execute("use library_server;");
            String qry="select * from borrower where fname='"+fname+"' and Lname='"+lname+"' and address='"+address+"';";

            ResultSet rs=stmt.executeQuery(qry);
            if(rs.next())
            {
                jLabel8.setForeground(Color.red);
                jLabel8.setText("There is already a user with same fname,lname,address");
                
            }
            else
            {
                int cn,crd_no=0;
                 
                 qry="select max(card_no) as MAX from borrower; ";
                 rs=stmt.executeQuery(qry);
                 while(rs.next())
                 {
                     crd_no=rs.getInt("MAX");
                     crd_no++;
                 }
                     cn=crd_no;
                    jLabel8.setForeground(Color.blue);
                    jLabel8.setText("all details are correct");
                    System.out.println("The values are"+cn+"\t"+fname+"\t"+lname+"\t"+address+"\t"+phn);
                    
                       int k=createborrower(conn,preparedstatement,cn,fname,lname,address,phn);
                        if(k==1)
                        {
                            JOptionPane.showMessageDialog(null,"Creation Successful"); 
                        }
                        else
                        {
                            JOptionPane.showMessageDialog(null,"some erro in creation","ERROR",JOptionPane.ERROR_MESSAGE);
                        }
                
            }
            
        }
            catch(SQLException e)
            {
                Logger.getLogger(search.class.getName()).log(Level.SEVERE, null, e);
            }
            
        }
        
       
